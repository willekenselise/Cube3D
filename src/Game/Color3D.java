package Game;

public class Color3D {
	private double red;
	private double green;
	private double blue;
	
	public Color3D(double red, double green, double blue) {
		this.red = red;
		this.green = green;
		this.blue = blue;
	}
	
	public double getRed() {
		return this.red;
	}
	
	public double getGreen() {
		return this.green;
	}
	
	public double getBlue() {
		return this.blue;
	}
	
}
